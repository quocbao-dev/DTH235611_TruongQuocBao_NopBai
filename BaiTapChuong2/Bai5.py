# Đây là ví dụ về các loại ghi chú trong Python

# 1. Ghi chú một dòng
x = 10  # Gán giá trị 10 cho biến x
y = 20  # Gán giá trị 20 cho biến y

# 2. Ghi chú nhiều dòng bằng nhiều dấu #
# Đoạn dưới đây sẽ tính tổng của x và y
# và in kết quả ra màn hình

z = x + y
print("Tổng của x và y là:", z)

# 3. Ghi chú nhiều dòng bằng chuỗi ký tự (''' hoặc """)
"""
Đây cũng là một cách ghi chú nhiều dòng.
Thực chất, Python tạo ra một chuỗi nhưng không dùng tới,
nên trình thông dịch sẽ bỏ qua.
"""
result = z * 2
print("Kết quả nhân đôi là:", result)
lst = [20, 1, -34, 40, -8, 60, 1, 3]

print("lst =", lst)
print("lst[0:3] =", lst[0:3])
print("lst[4:8] =", lst[4:8])
print("lst[4:33] =", lst[4:33])
print("lst[-5:-3] =", lst[-5:-3])
print("lst[-22:3] =", lst[-22:3])
print("lst[4:] =", lst[4:])
print("lst[:] =", lst[:])
print("lst[4] =", lst[4])
print("lst[1:5] =", lst[1:5])
print("-34 in lst =", -34 in lst)
